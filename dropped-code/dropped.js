// Init scrollspy
    $('body').scrollspy({ target: '#main-menu' })

    // Apply smooth scroll
    $("#main-menu a").on('click', function (event) {
      if (this.hash !== "") {
        event.preventDefault();

        const hash = this.hash;

        $('html, body').animate({
          scrollTop: $(hash).offset().top
        }, 800, function () {

          window.location.hash = hash;
        });
      }
    });